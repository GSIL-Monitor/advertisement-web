package com.yuanshanbao.dsp.partner.agent.feifan.model;

public class Impression {
	private String id;
	private Integer bidfloor;
	private String posId;
	private Integer type;
	private Integer s1;
	private Integer s2;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Integer getBidfloor() {
		return bidfloor;
	}

	public void setBidfloor(Integer bidfloor) {
		this.bidfloor = bidfloor;
	}

	public String getPosId() {
		return posId;
	}

	public void setPosId(String posId) {
		this.posId = posId;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getS1() {
		return s1;
	}

	public void setS1(Integer s1) {
		this.s1 = s1;
	}

	public Integer getS2() {
		return s2;
	}

	public void setS2(Integer s2) {
		this.s2 = s2;
	}
}
